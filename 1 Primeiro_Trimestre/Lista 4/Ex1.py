print("Números inteiros de 0 a 100:")
for i in range(101):
    print(i, end=" ")
print("\n")

print("Números pares de 20 a 50:")
for i in range(20, 51, 2):
    print(i, end=" ")
print("\n")

print("Números inteiros de 25 a 70 em ordem decrescente:")
for i in range(70, 24, -1):
    print(i, end=" ")
print("\n")

print("Números ímpares de 25 a 95 em ordem decrescente:")
for i in range(95, 24, -2):
    print(i, end=" ")
print("\n")